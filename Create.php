	// Function to add text to the t-shirt canvas with selected styles
// Function to apply text formatting to the text on canvas
function applyTextFormatting() {
    var fontSize = document.getElementById("font-size").value;
    var fontColor = document.getElementById("font-color").value;
    var textAlign = document.getElementById("text-align").value;

    // Apply formatting to text on canvas
    var canvas = document.getElementById("tcanvas");
    var context = canvas.getContext("2d");
    context.font = fontSize + "px Arial";
    context.fillStyle = fontColor;
    context.textAlign = textAlign;
}

// Event listener for the "Apply Format" button
document.getElementById("apply-format").addEventListener("click", function() {
    applyTextFormatting();
});

// Function to add text to canvas
function addTextToCanvas() {
    var text = document.getElementById("text-string").value;

    // Get canvas and context
    var canvas = document.getElementById("tcanvas");
    var context = canvas.getContext("2d");

    // Clear canvas
    context.clearRect(0, 0, canvas.width, canvas.height);

    // Apply formatting
    applyTextFormatting();

    // Add text to canvas
    context.fillText(text, canvas.width / 2, canvas.height / 2);
}

// Event listener for the "Add text" button
document.getElementById("add-text").addEventListener("click", function() {
    addTextToCanvas();
});

